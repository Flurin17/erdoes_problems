#!/usr/bin/env python3
"""Boundary order obstruction for Beeson's isosceles-alpha+beta branch.

This checks the `3alpha+2beta=pi` outer-triangle case with angles
`(alpha+beta, alpha+beta, alpha)`.  In this branch the two equal outer sides
have length `X=mu*c` and the base has length `Y=mu*a`.

The strict straight-boundary transition table forces any boundary side
containing an `a` edge to consist entirely of `a` edges.  The obstruction below
promotes that strict conclusion to non-strict boundary tilings only when every
side-length difference is absent from the semigroup generated by the tile side
lengths; then shifted side contacts cannot absorb a forbidden transition.
"""

from __future__ import annotations

import argparse
from functools import lru_cache

from beeson_isosceles_alpha_plus_beta_filter import Candidate
from beeson_isosceles_alpha_plus_beta_filter import base_count_triples
from beeson_isosceles_alpha_plus_beta_filter import candidates_for_n
from beeson_isosceles_alpha_plus_beta_filter import edge_count_triples


def representable(length: int, sides: tuple[int, int, int]) -> bool:
    """Return whether `length` is a nonnegative sum of the tile side lengths."""
    if length < 0:
        return False
    a, b, c = sides

    @lru_cache(maxsize=None)
    def rec(remaining: int) -> bool:
        if remaining == 0:
            return True
        return any(side <= remaining and rec(remaining - side) for side in (a, b, c))

    return rec(length)


def nonfit_differences(sides: tuple[int, int, int]) -> tuple[int, ...]:
    """Return positive side differences that cannot be tiled by full side lengths."""
    a, b, c = sides
    diffs = tuple(sorted({abs(a - b), abs(a - c), abs(b - c)} - {0}))
    return tuple(diff for diff in diffs if not representable(diff, sides))


def all_differences_nonfit(sides: tuple[int, int, int]) -> bool:
    a, b, c = sides
    diffs = tuple(sorted({abs(a - b), abs(a - c), abs(b - c)} - {0}))
    return bool(diffs) and len(nonfit_differences(sides)) == len(diffs)


def mixes_a(triple: tuple[int, int, int]) -> bool:
    p, q, r = triple
    return p > 0 and q + r > 0


def candidate_count_triples(candidate: Candidate) -> tuple[
    list[tuple[int, int, int]],
    list[tuple[int, int, int]],
]:
    a, b, c = candidate.sides
    equal = edge_count_triples(candidate.equal_side, a, b, c)
    base = base_count_triples(candidate.base, a, b, c, candidate.M)
    return equal, base


def boundary_order_obstructed(candidate: Candidate) -> bool:
    """Return whether the local boundary order/nonfit obstruction applies."""
    if resonant_240_obstructed(candidate):
        return True
    if not all_differences_nonfit(candidate.sides):
        return False
    equal, base = candidate_count_triples(candidate)
    if not equal or not base:
        return False
    return all(mixes_a(triple) for triple in equal) and all(mixes_a(triple) for triple in base)


def resonant_240_obstructed(candidate: Candidate) -> bool:
    """Special overhang check for the `(4,15,16)` survivor.

    Here the only representable side difference is `c-a=12=3a`.  Even if every
    `a`/`c` straight transition is allowed as an overhang rescue, the two
    `alpha+beta` base corners force an equal side endpoint of type
    `b:gamma->alpha`.  That endpoint can only follow itself, so a side ending
    there would have to be all `b` edges.  The allowed equal-side triples for
    the survivor contain no such word.
    """
    if candidate.sides != (4, 15, 16):
        return False
    equal, base = candidate_count_triples(candidate)
    if not equal or not base:
        return False
    if any(q > 0 for _p, q, _r in base):
        return False
    return all(not (p == 0 and r == 0) for p, _q, r in equal)


def obstruction_summary(candidate: Candidate) -> str:
    equal, base = candidate_count_triples(candidate)
    a, b, c = candidate.sides
    diffs = tuple(sorted({abs(a - b), abs(a - c), abs(b - c)} - {0}))
    fit = tuple(diff for diff in diffs if representable(diff, candidate.sides))
    status = "obstructed" if boundary_order_obstructed(candidate) else "survives"
    reason = ""
    if resonant_240_obstructed(candidate):
        reason = "; resonant_ac_overhang_endpoint_obstruction"
    return (
        f"M={candidate.M} sides={candidate.sides} X={candidate.equal_side} Y={candidate.base}: "
        f"{status}; diffs={diffs}; representable_diffs={fit}; "
        f"equal_triples={equal}; base_triples={base}{reason}"
    )


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("n", nargs="*", type=int)
    parser.add_argument("--range", dest="range_", nargs=2, type=int, metavar=("LO", "HI"))
    args = parser.parse_args()

    ns = args.n
    if args.range_:
        lo, hi = args.range_
        ns.extend(range(lo, hi + 1))
    if not ns:
        ns = [48, 132, 156, 175, 189, 198, 204, 224, 228, 240]

    for n in ns:
        candidates = candidates_for_n(n)
        print(f"N={n}: {len(candidates)} isosceles-alpha+beta candidate(s)")
        for candidate in candidates:
            print(f"  {obstruction_summary(candidate)}")


if __name__ == "__main__":
    main()
